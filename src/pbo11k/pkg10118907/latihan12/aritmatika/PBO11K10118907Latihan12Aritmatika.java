        int b = 20;
        int c = 25;
        int d = 25;
        
        System.out.println("a + b = "+ (a + b));
        System.out.println("a - b = "+ (a - b));
        System.out.println("a * b = "+ (a * b));
        System.out.println("b / s = "+ (b / a));
        System.out.println("b % a = "+ (b % a));
        System.out.println("c % a = "+ (c % a));
